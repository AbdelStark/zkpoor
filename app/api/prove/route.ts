import { NextRequest, NextResponse } from 'next/server';

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    
    // Forward request to Rust backend
    const response = await fetch('http://localhost:8080/prove', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(body),
    });

    if (!response.ok) {
      const errorData = await response.json().catch(() => ({ error: 'Unknown error' }));
      return NextResponse.json(errorData, { status: response.status });
    }

    const data = await response.json();
    return NextResponse.json(data);
  } catch (error) {
    console.error('Prove API error:', error);
    return NextResponse.json(
      { error: 'Failed to generate proof', details: error instanceof Error ? error.message : 'Unknown error' },
      { status: 500 }
    );
  }
}